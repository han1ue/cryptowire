import fs from "node:fs/promises";
import path from "node:path";

const repoRoot = process.cwd();
const sourceFile = path.join(repoRoot, "packages/types/src/sources.ts");
const outputFile = path.join(repoRoot, "apps/docs/references/sources.md");

const sourceText = await fs.readFile(sourceFile, "utf8");
const entryPattern = /\{\s*id:\s*"([^"]+)"\s*,\s*name:\s*"([^"]+)"\s*\}/g;

const entries = [];
for (const match of sourceText.matchAll(entryPattern)) {
    const id = match[1]?.trim() ?? "";
    const name = match[2]?.trim() ?? "";
    if (!id || !name) continue;
    entries.push({ id, name });
}

if (entries.length === 0) {
    throw new Error(`No sources parsed from ${sourceFile}`);
}

const lines = [
    "# Sources",
    "",
    "> This file is generated by `npm run docs:sync-sources`. Do not edit manually.",
    "",
    "Use these source ids anywhere a `sources` parameter is accepted:",
    "",
    "| Source ID | Name |",
    "| --- | --- |",
    ...entries.map((entry) => `| \`${entry.id}\` | ${entry.name} |`),
    "",
    "Fetch the live source list at any time:",
    "",
    "```sh",
    "curl \"https://api.cryptowi.re/news/sources\"",
    "```",
    "",
];

await fs.writeFile(outputFile, lines.join("\n"), "utf8");

console.log(`Updated ${path.relative(repoRoot, outputFile)} from ${path.relative(repoRoot, sourceFile)}`);
